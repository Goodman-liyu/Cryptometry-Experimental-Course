# 字节代换
def sub_bytes(state,x):
    if x=='state':
        for i in range(4):
            for j in range(4):
                state[i][j] = s_box[state[i][j]]
    elif x=='key':
        for i in range(4):
            state[i] = s_box[state[i]]


# 轮密钥加
def add_round_key(state, round_key):
    for i in range(4):
        for j in range(4):
            state[j][i] ^= round_key[i][j]

def get_Nr(Nb, Nk):
    if Nk == 4:
        return 10
    elif Nk == 6:
        return 12
    elif Nk == 8:
        return 14
    else:
        raise ValueError("Nk大小无效")

# 密钥扩展
def key_expansion(key, Nk, Nb, Nr):
    W = [[0 for _ in range(4)] for _ in range(Nb * (Nr + 1))]
    for i in range(Nk):
        for j in range(4):
            W[i][j] = key[j + 4 * i]

    for I in range(Nk, Nb * (Nr + 1)):
        temp = W[I - 1][:]
        if I % Nk == 0:
            temp=Rotl(temp)
            sub_bytes(temp,'key')
            temp = list_xor(temp, Rcon(I // Nk-1))
        elif I % Nk == 4 and Nk > 6:
            sub_bytes(temp,'key')
        W[I] = list_xor(W[I - Nk][:], temp)

    return W

def list_xor(a, b):
    if len(a) != len(b):
        raise ValueError("list长度不符")
    r = [x ^ y for x, y in zip(a, b)]
    return r

def Rotl(temp):
    return [temp[1], temp[2], temp[3], temp[0]]


def shift(x):
    t = [0] * 8
    t[1:8] = x[0:7]
    t[0] = x[7]
    return t

def Rcon(i):
    return [RC(i), 0, 0, 0]

def RC(i):
    if i == 0:
        return 1
    else:
        return xtime(RC(i - 1))

def shift_rows(state, Nb):
    if Nb == 4 or Nb == 6:
        bias = [0, 1, 2, 3]
    elif Nb == 6:
        bias = [0, 1, 3, 4]
    for i in range(1, 4):
        state[i] = state[i][bias[i]:] + state[i][:bias[i]]


